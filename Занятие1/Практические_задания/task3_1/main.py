number = 25

d = number // 10  # используя оператор целочисленного деления найти количество десятков
e = number % 10  # используя оператор остаток от деления найти количество единиц

print(d, e)  # команда print может принимать сразу несколько переменных для печати
print(d+e)  # вычисления можно производить прямо в функции print
